package com.example.jcrypto.model;

public class CoinModel {
    String id, icon, name, symbol, price;

    public CoinModel(String id, String icon, String name, String symbol, String price) {
        this.id = id;
        this.icon = icon;
        this.name = name;
        this.symbol = symbol;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
